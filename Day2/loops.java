public class loops {
    public static void main(String[] args) {
        // For Loop
        for(int i=0;i<6;i++){
            System.out.println(i + " ");
        }
        
        // While Loop
        int i = 1;
        while (i <= 5) {
            System.out.println(i + " ");
            i++;
        }

        // Do While Loop
        int k = 10;
        do {
            System.out.println(k + " ");
            k++;
        }while (k<=5);
    }
}
